GET METHOD
URL:
https://mysms.celcomafrica.com/api/services/sendsms/? 

GET PARAMS:
apikey : Valide API KEY. Get this by clicking the button above "GET API KEY & PARTNER ID"
partnerID : Valid Partner ID. Get this by clicking the button above "GET API KEY & PARTNER ID"
message : URL Encoded Text Message with valid GSM7 Characters 
shortcode : Valid Sender ID / Shortcode
mobile : Valid Mobile Number
SAMPLE REQUEST IN PHP

$partnerID = "useraccountpartnerId";
$apikey = "useraccountapikey";
$shortcode = "INFOTEXT";

$mobile = "254712345678"; // Bulk messages can be comma separated
$message = "This is a test message + = # special characters @ _ -";

$finalURL = "https://mysms.celcomafrica.com/api/services/sendsms/?apikey=" . urlencode($apikey) . "&partnerID=" . urlencode($partnerID) . "&message=" . urlencode($message) . "&shortcode=$shortcode&mobile=$mobile";
$ch = curl_init();
\curl_setopt($ch, CURLOPT_URL, $finalURL);
curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);
$response = curl_exec($ch);
curl_close($ch);
echo "Response: $response";
